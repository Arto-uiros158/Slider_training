		responsive: [
			{
				breakpoint: 768,
				settings: {

				}
			},
			{
				breakpoint: 480,
				settings: {

				}
			}
		], /* адаптив слайдера по брейкпоинтам */
		mobileFirst: false, /* меняет "направление" брейкпоинтов (с max-width на min-width) */
		// appendArrows: $('.anyblock'),
		// appendDots: $('.anyblock'), /* позволяет перемещать стрелки и точки в любой блок в html */
