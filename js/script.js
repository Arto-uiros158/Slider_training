		touchMove: true, /* слайды будут перелистываться при свайпе, но визуального движения кадров не будет */
		waitForAnimate: false, /* позволяет перелистывать слайды, не дожидаясь конца анимации прокрутки */
		centerMode: true, /* размещает 1й слайд по центру */
		variableWidth: true, /* "автоматический адаптив" слайдера: все слайды выстраиваются друг за другом исходя из своей ширины, а те, которые не помещаются в слайдер просто обрезаются */
		rows: 1, /* количество строк в слайдере (слайдер становится "многоэтажным") */
		slidesPerRow: 1, /* количество слайдов в строке */
		vertical: false, /* вертикальный слайдер */
		verticalSwiping: false, /* вертикальная прокрутка для вертикального слайдера */
		fade: false, /* плавная замена одного слайда на другой, как в слайд-шоу (хорошо работает, если для одного отцентрованного слайда в строке) */
